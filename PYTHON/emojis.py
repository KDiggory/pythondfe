# to use unicodes to print emojis we replace the + with 000 in the list of unicodes. 
#this is then preceeded by a \ and in quotes. 
print("\U0001F600") # smiley face
print("\U0001F603") # prints same smiley face with big eyes
print("\U0001F604") # smiley face with smiling eyes
print("\U0001F601") # smiley face with teeth, smiling eyes 
print("\U0001F605") # grinning squinting face
print("\U0001F970") # face with hearts around


# can also print them using CLDR short names
print("\N{winking face}") # prints a winking face




